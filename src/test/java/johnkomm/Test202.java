package johnkomm;

public class Test202
{
	public static void main(String[] args)
	{
		int x=10; //1010 is binary
		int y=20; //10100 is binary
		
		// bitwise and 
        System.out.println("x&y="+(x&y)); //0
  
        // bitwise or 
        System.out.println("x|y="+(x|y)); //30
  
        // bitwise xor 
        System.out.println("x^y="+(x^y)); //30
	}
}


